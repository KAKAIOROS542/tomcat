package modelo;

/**
 *
 * @author andres
 */
public class productoDTO {
    private int codigo;
    private String nombre;
    private int precioventa;
    private int ultprecio;
    private String descripcion;
    private int numeroexistencias;

    public int getCodigo() {
        return codigo;
    }

    public void setCodigo(int codigo) {
        this.codigo = codigo;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getPrecioventa() {
        return precioventa;
    }

    public void setPrecioventa(int precioventa) {
        this.precioventa = precioventa;
    }

    public int getUltprecio() {
        return ultprecio;
    }

    public void setUltprecio(int ultprecio) {
        this.ultprecio = ultprecio;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    public int getNumeroexistencias() {
        return numeroexistencias;
    }

    public void setNumeroexistencias(int numeroexistencias) {
        this.numeroexistencias = numeroexistencias;
    }

    
   
     
  
    
}
